#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;

	dup = (char *)malloc(ft_strlen(s1) + 1);
	if (!dup)
		return (0);
	ft_memcpy(dup, s1, ft_strlen(s1) + 1);
	return (dup);
}
